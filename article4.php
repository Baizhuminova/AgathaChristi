            <h2>Queen of Crime: Agatha’s influence</h2>
            <h5>When her car was found abandoned at a beauty spot close to her home in Surrey on 3 December 1926, Agatha Christie’s disappearance prompted a nationwide search involving over 1,000 people – including fellow crime writers Dorothy L. Sayers and Sir Arthur Conan Doyle.</h5>
            <div class="fakeimg"><img class="img1" src="img/B72842E0-2AB2-4366-B06390A898B921D0_sour.jpeg"> </div>
            <br>
            <p class="para">With newspapers offering rewards for information, speculation was rife, with her philandering husband, Archie, suspected of foul play. Eleven days after she disappeared, Christie was recognised at the Swan Hydro Hotel in Harrogate. She had signed in under the name of Teresa Neele, the surname being that of her husband’s mistress, Nancy Neele. Whether Christie’s disappearance was an act of revenge on her husband or her attempt to be alone, this episode will remain the Queen of Crime’s most intriguing mystery.</p>
            <br>
            <p class="para">Here, three crime-writing authors reveal the influence Agatha Christie had on their own work:</p> <br>
            <h3 class="para">Linda Stratmann</h3>

            <p class="para">Author of the Frances Doughty series</p> <br>
            <p class="para">‘I love a good puzzle mystery, and Agatha Christie is a huge inspiration to my writing. She is a brilliant exponent of the art of misdirection and disguise, but never cheats the reader. All the information needed to solve the murder is provided through elegant plotting and deftly placed clues. Deceptively easy to read, her stories contain a wealth of fine detail and insights into human nature, and the final revelation dovetails all the pieces into a perfect solution. Her books remain classics of the genre. She, like myself, was once a chemist’s dispenser, and I especially admire her realistic use of poisons.’</p>
            <br>
            <h3 class="para">Martin Edwards </h3>

            <p class="para">Editor of Best Eaten Cold & Other Stories and Truly Criminal</p> <br>
            <p class="para">‘Agatha Christie was the first writer of fiction for adults whom I read – as I was coming up to my ninth birthday. In The Golden Age of Murder, I tell the full story of how this came about. Suffice to say here that I began with Murder at the Vicarage, and from that day my heart was set on becoming a detective novelist myself. To date I’ve managed eighteen crime novels, and sixty short stories. I’ll never outsell Agatha, but I’ve followed in her footsteps in one other respect. She was the fourth President of the Detection Club, which she helped to found in 1930. And I’m the eighth...’</p>
            <br>
            <h3 class="para">C.B. Hanley</h3>

            <p class="para">Author of the Mediaeval Mystery series</p> <br>
            <p class="para">‘How has Agatha Christie influenced my writing? In many ways, is the short answer! I have a complete set of Christie’s books here which I have re-read several times, as well as various biographies and a fascinating work about her handwritten notebooks, which illustrates how she honed her narrative techniques. Obviously I’m writing in a slightly different genre – medieval mysteries rather than contemporary ones – but I aspire to learn from her peerless plotting. Read one of Christie’s books twice and you’ll see that the clues were staring you in the face all along: you just didn’t see them because her misdirection is so good.’ </p>
