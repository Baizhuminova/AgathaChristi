            <h2>How Agatha Christie turns readers into bestselling novelists</h2>
            <h5>When authors talk about formative childhood books, few names sprout up more than Agatha Christie’s.
                We asked Lisa Jewell, John Boyne, and a host of others:
                What is it about her mysteries that inspire readers to become writers?</h5>
            <div class="fakeimg"><img class="img1" src="img/81d41e6b84304363f29a9af9805a130f.jpeg"> </div>
            <br>
            <p class="para">Whenever debut novelist Frances Cha goes home to Korea for the summer, she embarks on a ritual:
                “I reread my entire Agatha Christie collection,” the author of If I Had Your Face says, “which is about 65 of her books.”
                Before she wrote a word of any of her hugely successful thrillers like The Night She Disappeared or The Family Upstairs,
                Lisa Jewell “stumbled upon Agatha Christie” in the library at about 12 years old, then “read all her books over the space of a year.”
                And just last month, author John Boyne tweeted,
                “Do you have a comfort author you return to when nothing in your To Be Read pile seems to be calling out to you?
                Here’s mine”, accompanied by a photo of his extensive bookshelf of Agatha Christie spines. </p>
            <br>
            <h3 class="para">Agatha Christie’s net worth</h3>
            <br>
            <p class="para">A century on from the beginning of her publishing career, Agatha Christie’s allure remains undiminished.
                And that’s saying something: referred to regularly as The Queen of Crime, her name has been synonymous with mystery and thriller novels
                since the mid-20th Century, when she was the first recipient of the Mystery Writers of America's Grand Master Award in
                1955; her characters, Hercule Poirot and Miss Marple, have become household names, thanks not just to the ubiquity of
                her books but the popularity of their adaptations to television, cinema
                (Death on the Nile was just released to international theatres this month), and other media.</p> <br>

            <p class="para">In the UK, she’s been honoured with statues and postage stamps, but she more than transcends her homeland:
                she remains the most internationally translated author in the world.
            </p>
            <br>
            <p class="para">Yet, she seems to have taken on an additional, more specific mantle recently:
                absorbing her works increasingly feels like an essential rite of passage for novelists,
                the first step on the road from young reader to published author.
            </p> <br>
            <p class="para">Christie comes up regularly in interviews, especially with debut novelists: not just Frances Cha but Sarah Pearse,
                whose debut The Sanatorium’s unpredictable, Christie-esque twists and turns caused it to become a bestseller
                in 2021. Asked if she could have dinner with any writer ever, Pearse confirmed,
                “It would have to be Agatha Christie – I would love to hear more about the inspiration for her plots.”</p> <br>
            <p class="para">Jewell says that it’s the structuring of those plots that makes Christie such a formative
                presence in the lives of young readers – and of writers-to-be.</p> <br>
            <p class="para">“Agatha Christie was a very mechanical writer; she knew exactly what she was doing with each and every move
                in the book. It was all pre-thought – nothing happens by accident in her books. It’s like the mastermind
                behind a crossword. For me as a reader, it was this addiction to the feeling of a puzzle just coming together,
                the clues appearing,
                and of trying to get one step ahead of her and knowing that I never ever, ever was going to get ahead of her.”</p>
            <br>
            <p class="para">Jewell compares the act of reading Christie as a 12-year-old to having a great teacher at school:
                “She manipulates you, but not in a sinister way; in a masterful way, being manipulated by someone
                who knows what's going
                on inside your brain and what switches to flick to make you feel the way that she wants to make you feel.”</p> <br>
