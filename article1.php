            <h2>Agatha Christie shaped how the world sees Britain</h2>
            <h5>The mystery writer is the world’s best-selling novelist and most translated author – so what are non-Brits learning about English people and culture through her stories?</h5>
            <div class="fakeimg"><img class="img1" src="img/Agatha-Christie.jpeg"> </div>
            <br>
            <p class="para">For many, Agatha Christie is as quintessentially English as queueing for Pimm’s at Wimbledon.
                But as the best-selling novelist in history, her reputation goes far beyond the UK.
                Her works are seen as so easy to read, she’s a favourite author of people learning English.
                Christie is also, by leaps and bounds, the world’s most translated author –
                so if many people are learning the English language through Christie,
                they are learning about the English people through her, too. </p>
            <br>
            <p class="para">This is obvious, for instance, in the Collins English Readers series of abridged Christie books,
                whose cultural notes explain such topics as English village life and the saying ‘No smoke without fire’.
                The Los Angeles-based podcast hosts Catherine Brobeck and Kemper Donovan agree.
                Brobeck and Donovan broadcast the podcast All About Agatha, which has the ambitious aim of discussing and ranking all of Christie’s 60-plus novels. </p>
            <br>
            <p class="para">For Brobeck, Christie was not only the first adult author she read heavily,
                but her first major exposure to English culture.
                “Of course that influences your idea of Englishness,” she says.
                If Agatha Christie is continuing to influence readers’ ideas of Englishness, what exactly are they learning
                – and is any of it true? Christie’s version of Englishness is “there to be played with, rather than grimly realistic,”
                says the University of Hull English lecturer Sabine Vanacker. </p> <br>

            <p class="para">As with other classic crime fiction, her settings and characteristics take some artistic licence and even have elements of caricature.
                In reality, of course, not every British village has a kindly vicar, a blowhard colonel, a brittle lawyer, and a twinkling doctor – or indeed a preponderance of murderers.
                There’s also a surprising lack of pubs in her stories. But not all of Christie’s version of Englishness is fanciful.
                “The self-deprecation to me is what really comes through,” says Donovan.
            </p>
            <br>
            <p class="para">He sees something particularly English about the way Christie, either through her narration or her characters, uses dry wit, often gallows humour. In the short story Triangle at Rhodes, for example, she writes of one Englishwoman abroad: “Unlike most English people, she was capable of speaking to strangers on sight instead of allowing four days to a week to elapse before making the first cautious advance as is the customary British habit.” </p>
            <br>
            <p class="para">This kind of humour was very much part of the author’s personality, says Janet Morgan, (author of an official biography of Agatha Christie). Morgan explains that while Christie had strong beliefs about good and evil. “She was a very humorous person… She was amused by life, and by human beings, and by how they behaved.”</p>
